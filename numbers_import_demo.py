import math

PI = -3.14
round_of_pi = round(PI)
print("round of PI: " + str(round_of_pi))
print()

num1 = 3
num2 = 2
max_number = max(2, 3)
print(f"max of {num1} , {num2}: " + str(max_number))
print()

min_number = min(2, 3)
print(f"min of {num1} , {num2}: " + str(min_number))
print()

factorial_of_3 = str(math.factorial(3))

